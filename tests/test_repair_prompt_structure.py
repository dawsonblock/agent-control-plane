# 3. Delegation: tests_missing=True routes to the test-gen prompt
# --------------------------------------------------------------------------- #


class TestRepairPromptDelegation:
    """write_repair_prompt with tests_missing=True delegates to test-gen."""

    def test_tests_missing_true_uses_test_gen_prompt(self, tmp_path: Path):
        """tests_missing=True produces the test-generation prompt, not the
        standard repair prompt."""
        cfg = _make_config()
        prompt = write_repair_prompt(
            original_request="Add feature",
            worktree_path=tmp_path / "wt",
            artifact_dir=tmp_path / "art",
            repo_config=cfg,
            failures=[],
            attempt=1,
            max_attempts=5,
            tests_missing=True,
        )
        body = prompt.read_text()
        # Test-gen prompt markers (not standard repair markers).
        assert "Test generation attempt 1 of 5" in body
        assert "Write unit tests" in body
        # Standard repair marker should NOT appear.
        assert "Repair attempt" not in body

    def test_tests_missing_false_uses_standard_prompt(self, tmp_path: Path):
        """tests_missing=False (default) produces the standard repair prompt."""
        cfg = _make_config()
        prompt = write_repair_prompt(
            original_request="Fix bug",
            worktree_path=tmp_path / "wt",
            artifact_dir=tmp_path / "art",
            repo_config=cfg,
            failures=_sample_failures(),
            attempt=1,
            max_attempts=5,
            tests_missing=False,
        )
        body = prompt.read_text()
        assert "Repair attempt 1 of 5" in body
        assert "Test generation attempt" not in body


