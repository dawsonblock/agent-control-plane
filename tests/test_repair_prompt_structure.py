"""Tests for repair-plan prompt structure (v0.6.8+).

Asserts that the repair prompts generated by ``write_repair_prompt``
and ``write_missing_tests_prompt`` match the documented spec — the
exact structure the LangGraph ``repair_plan_node`` builds when a task
fails validation or when the RiskEngine flags TESTS_MISSING.

These are structure/contract tests, not end-to-end loop tests (those
live in ``test_repair_loop.py``). They verify the prompt the agent
receives is well-formed, contains the failure signal, and enforces the
non-negotiable constraints.

Covers:
  1. Standard repair prompt (Scenario A — tests failing)
  2. Dynamic test-generation prompt (Scenario B — TESTS_MISSING)
  3. Failure-block formatting (stdout/stderr/exit_code/command)
  4. Attempt numbering and max-attempts display
  5. Runtime commands section
  6. Delegation: tests_missing=True routes to the test-gen prompt
  7. Circuit breaker config field exists and defaults correctly
"""

from __future__ import annotations

from pathlib import Path

import pytest

from acp.agents.base import write_missing_tests_prompt, write_repair_prompt
from acp.config import (
    AgentSection,
    CommandsSection,
    FederationServerConfig,
    FederationSection,
    RepoConfig,
    RepoSection,
    ReviewSection,
)


# --------------------------------------------------------------------------- #
# Helpers
# --------------------------------------------------------------------------- #


def _make_config(
    *,
    test_cmd: str = "pytest",
    lint_cmd: str = "ruff check .",
    typecheck_cmd: str = "mypy .",
    federation_servers: list | None = None,
) -> RepoConfig:
    """Build a minimal RepoConfig for prompt-generation tests."""
    fed = FederationSection()
    if federation_servers:
        fed = FederationSection(servers=[
            FederationServerConfig(name=s[0], command=s[1]) for s in federation_servers
        ])
    return RepoConfig(
        repo=RepoSection(name="my-app", path=Path("/tmp/test"), default_branch="main"),
        commands=CommandsSection(
            test=test_cmd,
            lint=lint_cmd,
            typecheck=typecheck_cmd,
        ),
        review=ReviewSection(),
        federation=fed,
    )


def _sample_failures() -> list[dict[str, object]]:
    """One realistic failing-command entry (matches the spec example)."""
    return [
        {
            "command": "pytest tests/test_auth.py",
            "exit_code": 1,
            "stdout": (
                "============================= test session starts ==="
                "==========================\n"
                "collected 1 item\n\n"
                "tests/test_auth.py F"
                "                                                     [100%]\n\n"
                "=================================== FAILURES ==="
                "==================================\n"
                "______________________________ test_login_success "
                "______________________________\n"
                "    def test_login_success():\n"
                ">       assert login(\"user\", \"pass\") == {\"status\": 200}\n"
                "E       AssertionError: assert {'status': 500} == {'status': 200}\n"
            ),
            "stderr": "",
        },
    ]


