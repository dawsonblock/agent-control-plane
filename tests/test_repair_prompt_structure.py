# 4. Circuit breaker config
# --------------------------------------------------------------------------- #


class TestCircuitBreakerConfig:
    """The circuit breaker config field exists and defaults correctly."""

    def test_repair_repeat_breaker_defaults_to_3(self):
        """Default breaker is 3 (stops after 3 identical failure signatures)."""
        cfg = AgentSection()
        assert cfg.repair_repeat_breaker == 3

    def test_repair_repeat_breaker_can_be_disabled(self):
        """Setting breaker to 0 disables it."""
        cfg = AgentSection(repair_repeat_breaker=0)
        assert cfg.repair_repeat_breaker == 0

    def test_repair_repeat_breaker_validates_range(self):
        """Breaker must be between 0 and 20."""
        with pytest.raises(ValueError, match="max_repair_attempts"):
            AgentSection(max_repair_attempts=-1)
        with pytest.raises(ValueError, match="max_repair_attempts"):
            AgentSection(max_repair_attempts=21)

    def test_max_repair_attempts_defaults_to_5(self):
        """Default max repair attempts is 5 (raised in v0.6.0)."""
        cfg = AgentSection()
        assert cfg.max_repair_attempts == 5
